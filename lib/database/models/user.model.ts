import { Document, Model, Schema, Types, model, models } from "mongoose";

export interface IUser {
  _id: Types.ObjectId;
  clerkId: string;
  email: string;
  username: string;
  photo: string;
  firstName?: string;
  lastName?: string;
  planId: number;
  creditBalance: number;
}

// Define the model interface with constructor
export type UserModel = Model<IUser & Document>;

const UserSchema = new Schema({
  clerkId: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  photo: {
    type: String,
    required: true,
  },
  firstName: {
    type: String,
  },
  lastName: {
    type: String,
  },
  planId: {
    type: Number,
    default: 1,
  },
  creditBalance: {
    type: Number,
    default: 10,
  },
});

// Use the custom UserModel type instead of Model<UserDocument>
const User = (models?.User || model("User", UserSchema)) as UserModel;
export default User;
